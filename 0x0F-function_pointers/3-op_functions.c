#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - Adds 2 integers
 * @a: number one
 * @b: number two
 *
 * Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract 2 integers
 * @a: number one
 * @b: number two
 *
 * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies 2 integers
 * @a: number one
 * @b: number two
 *
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides 2 integers
 * @a: number one
 * @b: number two
 *
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulus
 * @a: number one
 * @b: number two
 *
 * Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
